macro en(str)
        %text_mapping()
        dw "<str>", $FFFE
        %tail_mapping()
        dw "<str>", $FFFF
endmacro

macro jp(str)
        %tail_mapping()
        dw "<str>", $FFFE
        %text_mapping()
        dw "<str>", $FFFF
endmacro

macro text(en_text, jp_text)
        dw ?en_label
        dw ?jp_label

        ?en_label: %en("<en_text>")
        ?jp_label: %jp("<jp_text>")
endmacro

macro lang_swap_text(en_text, jp_text)
        dw ?en_label
        dw ?jp_label

        ?en_label: %jp("<en_text>")
        ?jp_label: %en("<jp_text>")
endmacro
